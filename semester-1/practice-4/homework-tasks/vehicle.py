"""
Модуль для демонстрации полиморфизма в Python.
Содержит иерархию классов транспортных средств.
"""


class Vehicle():
    """
    Базовый класс для всех транспортных средств.

    Атрибуты:
        Нет явных атрибутов, но может быть расширен
    """

    def info(self):
        """
        Возвращает описание транспортного средства.

        Returns:
            str: Описание транспортного средства
        """
        pass  # Абстрактный метод, должен быть переопределен в дочерних классах


class Moto(Vehicle):
    """
    Класс, представляющий мотоцикл.

    Наследует от класса Vehicle.
    """

    def info(self):
        """
        Возвращает описание мотоцикла.

        Returns:
            str: Строка с описанием мотоцикла
        """
        return 'я мотоцикл'


class Car(Vehicle):
    """
    Класс, представляющий автомобиль.

    Наследует от класса Vehicle.
    """

    def info(self):
        """
        Возвращает описание автомобиля.

        Returns:
            str: Строка с описанием автомобиля
        """
        return 'я машина'


# Демонстрация работы полиморфизма

print(Car().info())   # Вывод: я машина
print(Moto().info())  # Вывод: я мотоцикл
